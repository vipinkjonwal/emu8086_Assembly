DATA SEGMENT
    TEMP DW 0
    PROD DW 1
    NUMBER DW 5
    FACTORIAL DW ?
DATA ENDS

CODE SEGMENT
    ASSUME DS:DATA,CS:CODE
    
START:
    MOV AX,DATA
    MOV DS,AX
    
    MOV AX,0
    
AGAIN:
    MOV BX,TEMP
    MOV DX,NUMBER
    CMP BX,DX
    JZ DONE
    INC TEMP
    MOV AX,TEMP
    MUL PROD
    MOV PROD,AX
    LOOP AGAIN    
        
DONE:
    MOV FACTORIAL,AX

CODE ENDS
END START